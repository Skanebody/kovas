/**
 * Helpers — lookup unifié sur registry + extras.
 */
const EXTRAS_BY_SLUG: ReadonlyMap<string, ExtraCity> = new Map(
  EXTRA_CITIES_TOP_5000.map((c) => [c.slug, c]),
)

/**
 * Trouve une ville (City complète) dans le registry OU dans les extras top-5000.
 * Retourne undefined si introuvable.
 */
export function findCityAnywhere(slug: string): City | undefined {
  const direct = getCityBySlug(slug)
  if (direct) return direct
  const extra = EXTRAS_BY_SLUG.get(slug)
  if (!extra) return undefined
  return extraToCity(extra)
}

/**
 * Tous les slugs des villes premium + extras (utilisé par generateStaticParams).
 * Garantit l'unicité (set deduplication).
 */
export function getAllTop5000Slugs(): ReadonlyArray<{ dept: string; city: string }> {
  const set = new Set<string>()
  const out: Array<{ dept: string; city: string }> = []
  for (const c of CITIES) {
    const key = `${c.dept}:${c.slug}`
    if (!set.has(key)) {
      set.add(key)
      out.push({ dept: c.dept, city: c.slug })
    }
  }
  for (const c of EXTRA_CITIES_TOP_5000) {
    const key = `${c.dept}:${c.slug}`
    if (!set.has(key)) {
      set.add(key)
      out.push({ dept: c.dept, city: c.slug })
    }
  }
  return out
}

/**
 * Statistiques top-5000 (registry + extras).
 */
export function getTop5000Stats(): {
  totalRegistry: number
  totalExtras: number
  totalUnique: number
  byRegion: Record<string, number>
} {
  const byRegion: Record<string, number> = {}
  const seenSlugs = new Set<string>()

  for (const c of CITIES) {
    if (seenSlugs.has(c.slug)) continue
    seenSlugs.add(c.slug)
    byRegion[c.region] = (byRegion[c.region] ?? 0) + 1
  }
  for (const c of EXTRA_CITIES_TOP_5000) {
    if (seenSlugs.has(c.slug)) continue
    seenSlugs.add(c.slug)
    byRegion[c.region] = (byRegion[c.region] ?? 0) + 1
  }

  return {
    totalRegistry: CITIES.length,
    totalExtras: EXTRA_CITIES_TOP_5000.length,
    totalUnique: seenSlugs.size,
    byRegion,
  }
}

