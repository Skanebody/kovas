/**
 * Forme allégée pour les villes "tier 2" (4000 villes 5k-30k habitants).
 * Pas de `neighbors` (calculés à la volée par proximité dept).
 * Pas de `lat/lng` (calculés via chef-lieu dept).
 *
 * Signature volontairement compatible avec `City` partial → upcastable.
 */
export interface ExtraCity {
  readonly slug: string
  readonly name: string
  readonly postalCode: string
  readonly dept: string
  readonly region: string
  readonly population: number
}

/**
 * Convertit une ExtraCity en City complète avec valeurs déduites pour ce
 * tier (lat/lng = chef-lieu dept approximatif, neighbors = autres villes
 * du même département, priority = 'low' par défaut).
 */
export function extraToCity(extra: ExtraCity): City {
  const cap = DEPT_CAPITALS[extra.dept]
  return {
    slug: extra.slug,
    name: extra.name,
    postalCode: extra.postalCode,
    inseeCode: extra.postalCode, // approximation — ne pas s'en servir pour requêtes INSEE
    dept: extra.dept,
    region: extra.region,
    population: extra.population,
    lat: cap?.lat ?? 46.5,
    lng: cap?.lng ?? 2.5,
    neighbors: [],
    priority: 'low' as CityPriority,
  }
}

